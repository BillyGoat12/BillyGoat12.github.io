// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  if(num1 === num2){// checking if num1 and num2 are the same
    return num1;// return num1
  }else if(num1 < num2){// checking if num1 is less then num2
    return num1;// return num1
  } else{// default
    return num2;// return num2
  }
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if(num % 2 === 0){// checking if num is even
    return true;// return true
  }// default
  return false;// return false
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, char) {
  let num = 0;// creating a varible with the value of zero
  for(let i = 0; i < string.length; i++){// looping over the string
    if(string[i] === char){// checking if char is equal to the charatacter at index i in string
      num += 1;// add one to num
    }
  }
  return num;// return num
}

